#include <stdio.h>

/**
 * main - Entry point
 *
 * Description: using do while loop statement
 *
 * Return: Always 0 (success)
 */

int main(void)
{
	int i = 6; /* initialization */

	do {
		printf("Hello Challo\n");
		i++; /* increment or decrement */
	} while (i <= 7); /* condition */

	return (0);
}
